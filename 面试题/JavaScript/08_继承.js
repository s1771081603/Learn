/**继承
 * 继承是什么
 *      继承（inheritace）是面向对象软件技术当中的一个概念。
 * 继承的优点：
 *      继承可以使得子类具有父类别的各种属性和方法，而不需要再次编写相同代码。
 *      子类继承父类的同时，可以重新定义某些属性，并重写这些方法，
 *      即覆盖父类的原有属性和方法，使其获得与父类不同的功能。
 *      JavaScript 并不是真正的面向对象语言，它天生的灵活性，应用场景更加丰富。
 * 
 * 实现方式：
 *      1.原型链继承
 *      2.构造函数继承
 *      3.组合继承
 *      4.原型式继承
 *      5.寄生式继承
 *      6.寄生组合式继承
 */